const ALL_SMELLS = Object.freeze([...TSDETECT_SMELLS, ...ESE_2025_SMELLS, ...CORTEX_ORIGINAL_SMELLS]);

function getSmellById(id) {
  return ALL_SMELLS.find((s) => s.id === id) || null;
}

function listSmellIds() {
  return ALL_SMELLS.map((s) => s.id);
}

function listByCategory(cat) {
  return ALL_SMELLS.filter((s) => s.category === cat);
}

function listByLanguage(lang) {
  return ALL_SMELLS.filter((s) => s.languages.includes(lang));
}

