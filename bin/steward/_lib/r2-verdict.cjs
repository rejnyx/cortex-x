'use strict';

const crypto = require('node:crypto');
const fs = require('node:fs');
const path = require('node:path');
const os = require('node:os');

const SCHEMA_VERSION = 1;
const SCHEMA_VERSION_V2 = 2;
const SUPPORTED_SCHEMA_VERSIONS = [SCHEMA_VERSION, SCHEMA_VERSION_V2];
const SIG_ALG = 'HMAC-SHA256';
const SIG_ALG_ED25519 = 'Ed25519';
const SUPPORTED_SIG_ALGS = [SIG_ALG, SIG_ALG_ED25519];

// ---------------------------------------------------------------------------
// canonicalize(value) — RFC 8785 JCS subset (strings/numbers/bool/null/obj/arr)
// ---------------------------------------------------------------------------
function canonicalize(value) {
  if (value === null) return 'null';
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  if (typeof value === 'number') {
    if (!Number.isFinite(value)) {
      throw new Error('CORTEX_R2_VERDICT_NON_FINITE_NUMBER');
    }
    return JSON.stringify(value);
  }
  if (typeof value === 'string') return JSON.stringify(value);
  if (Array.isArray(value)) {
    // Match JSON.stringify semantics: undefined array slots serialize as null.
    return (
      '[' +
      value
        .map((v) => (v === undefined ? 'null' : canonicalize(v)))
        .join(',') +
      ']'
    );
  }
  if (typeof value === 'object') {
    // Match JSON.stringify semantics: drop keys whose value is undefined.
    const keys = Object.keys(value)
      .filter((k) => value[k] !== undefined)
      .sort();
    const parts = keys.map(
      (k) => JSON.stringify(k) + ':' + canonicalize(value[k])
    );
    return '{' + parts.join(',') + '}';
  }
  throw new Error('CORTEX_R2_VERDICT_UNSUPPORTED_TYPE:' + typeof value);
}

// ---------------------------------------------------------------------------
// _resolveDataHome() — delegates to the canonical SSOT
// `tools/lib/resolve-cortex-home.cjs::resolveCortexDataHome`.
//
// Sprint 2.46.1 R2 fix HIGH-4: previously three independent implementations
// existed (this file, r2-verdict-keys.cjs, tools/lib/resolve-cortex-home.cjs)
// with divergent Windows behavior (some honored %APPDATA%, some did not, none
// read cortex-source.yaml). On Windows with no env var, the HMAC secret and
// the Ed25519 key would resolve to DIFFERENT directories — cryptographic
// trust roots split. All three now go through the canonical resolver.
// ---------------------------------------------------------------------------
let _resolveCortexDataHome;
function _resolveDataHome() {
  if (!_resolveCortexDataHome) {
    try {
      _resolveCortexDataHome = require(path.join(__dirname, '..', '..', '..', 'tools', 'lib', 'resolve-cortex-home.cjs')).resolveCortexDataHome;
    } catch {
      // Fallback for installations where tools/lib/ is not present
      // (e.g. distributed cortex-x without the dev tree). Uses the env+home
      // shape — the cortex-source.yaml branch is unreachable here, which is
      // acceptable degradation outside the dev tree.
      _resolveCortexDataHome = () => {
        if (process.env.CORTEX_DATA_HOME && process.env.CORTEX_DATA_HOME.length > 0) {
          return process.env.CORTEX_DATA_HOME;
        }
        return path.join(os.homedir(), '.cortex');
      };
    }
  }
  return _resolveCortexDataHome();
}

function _secretFilePath() {
  return path.join(_resolveDataHome(), 'r2-verdict', 'hmac.key');
}

// ---------------------------------------------------------------------------
// resolveSecret() — hybrid resolution v2:
//   (1) env CORTEX_R2_VERDICT_SECRET                 → source: 'env'
//   (2) $CORTEX_DATA_HOME/r2-verdict/hmac.key file   → source: 'file'
//       (auto-generate on first use; 0600 on POSIX, silent on win32)
//   (3) host-derived sha256(hostname|username)       → source: 'host-derived'
//
// Returns { secret, source }. Caller decides STRICT policy.
// The file-generate path uses crypto.randomBytes — this is an INPUT-side
// operation (one-time bootstrap); the deterministic signing path consumes
// the resolved secret string only.
// ---------------------------------------------------------------------------
function resolveSecret() {
  const fromEnv = process.env.CORTEX_R2_VERDICT_SECRET;
  if (fromEnv && fromEnv.length > 0) {
    return { secret: fromEnv, source: 'env' };
  }

  // (2) persisted file path
  const filePath = _secretFilePath();
  let fileSecret = null;
  try {
    fileSecret = fs.readFileSync(filePath, 'utf8').trim();
  } catch (err) {
    if (!err || err.code !== 'ENOENT') {
      // I/O error other than missing — fall through to host-derived.
      fileSecret = null;
    }
  }
  if (fileSecret && fileSecret.length > 0) {
    return { secret: fileSecret, source: 'file' };
  }

  // First-use bootstrap: generate, persist, return.
  // Gated behind missing-file check; idempotent on second invocation.
  try {
    const generated = crypto.randomBytes(32).toString('hex');
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, generated, 'utf8');
    if (process.platform !== 'win32') {
      try {
        fs.chmodSync(filePath, 0o600);
      } catch (_) {
        /* best-effort */
      }
    }
    return { secret: generated, source: 'file' };
  } catch (_) {
    // mkdir / write failed (read-only fs / sandbox) — fall through.
  }

  // (3) Host-derived fallback: not cryptographically strong against other
  // users on the same machine, but enough for single-operator local continuity.
  const host = os.hostname() || 'unknown-host';
  let user = 'unknown-user';
  try {
    user = os.userInfo().username || 'unknown-user';
  } catch (_) {
    /* userInfo can throw in some sandboxes */
  }
  const fallback = crypto
    .createHash('sha256')
    .update(host + '|' + user)
    .digest('hex');
  return { secret: fallback, source: 'host-derived' };
}

// ---------------------------------------------------------------------------
// _resolveSecretNoGenerate() — same as resolveSecret() but never auto-creates
// the persisted file. Used by STRICT_SECRET path so missing-secret is visible.
// ---------------------------------------------------------------------------
function _resolveSecretNoGenerate() {
  const fromEnv = process.env.CORTEX_R2_VERDICT_SECRET;
  if (fromEnv && fromEnv.length > 0) {
    return { secret: fromEnv, source: 'env' };
  }
  const filePath = _secretFilePath();
  try {
    const data = fs.readFileSync(filePath, 'utf8').trim();
    if (data && data.length > 0) {
      return { secret: data, source: 'file' };
    }
  } catch (_) {
    /* fall through */
  }
  const host = os.hostname() || 'unknown-host';
  let user = 'unknown-user';
  try {
    user = os.userInfo().username || 'unknown-user';
  } catch (_) {
    /* ignore */
  }
  const fallback = crypto
    .createHash('sha256')
    .update(host + '|' + user)
    .digest('hex');
  return { secret: fallback, source: 'host-derived' };
}

// ---------------------------------------------------------------------------
// signPayload(payload, secret) → hex HMAC-SHA256 over canonical JSON
// ---------------------------------------------------------------------------
function signPayload(payload, secret) {
  const canonical = canonicalize(payload);
  return crypto
    .createHmac('sha256', secret)
    .update(canonical, 'utf8')
    .digest('hex');
}

// ---------------------------------------------------------------------------
// _toBase64Url(buf) — RFC 4648 §5, no padding.
// ---------------------------------------------------------------------------
function _toBase64Url(buf) {
  return Buffer.from(buf)
    .toString('base64')
    .replace(/\+/g, '-')
    .replace(/\//g, '_')
    .replace(/=+$/g, '');
}

function _fromBase64Url(str) {
  const padded = str.replace(/-/g, '+').replace(/_/g, '/');
  const pad = padded.length % 4 === 0 ? '' : '='.repeat(4 - (padded.length % 4));
  return Buffer.from(padded + pad, 'base64');
}

// ---------------------------------------------------------------------------
// signPayloadEd25519(payload, privateKeyPem) → base64url string
// crypto.sign(null, msg, key) is the Ed25519 API per node docs (alg=null,
// because Ed25519 uses its own hash internally).
// ---------------------------------------------------------------------------
function signPayloadEd25519(payload, privateKeyPem) {
  const canonical = canonicalize(payload);
  const sig = crypto.sign(null, Buffer.from(canonical, 'utf8'), {
    key: privateKeyPem,
    format: 'pem',
  });
  return _toBase64Url(sig);
}

function verifyPayloadEd25519(payload, sigB64Url, publicKeyPem) {
  const canonical = canonicalize(payload);
  let sigBuf;
  try {
    sigBuf = _fromBase64Url(sigB64Url);
  } catch (_) {
    return false;
  }
  try {
    return crypto.verify(
      null,
      Buffer.from(canonical, 'utf8'),
      { key: publicKeyPem, format: 'pem' },
      sigBuf
    );
  } catch (_) {
    return false;
  }
}

// ---------------------------------------------------------------------------
// _lazyKeysModule() — soft-load sibling module so tests / callers without
// the keys module fail with a clear error rather than at require() time.
// ---------------------------------------------------------------------------
function _lazyKeysModule() {
  try {
    // eslint-disable-next-line global-require
    return require('./r2-verdict-keys.cjs');
  } catch (_) {
    return null;
  }
}

// ---------------------------------------------------------------------------
// buildVerdict(input) → { ...payload, signature: { alg, value, ... } }
//
// v1 (legacy, default when neither commitSha nor signatureAlgorithm='Ed25519'
// is supplied): exact Sprint 2.46 shape, schema_version=1, no commit_sha,
// signature = { alg: 'HMAC-SHA256', value: <hex> }.
//
// v2 (Sprint 2.46.1): triggered by passing `commitSha` OR explicit
// `schemaVersion: 2` OR `signatureAlgorithm: 'Ed25519'`. Adds:
//   - commit_sha (REQUIRED)
//   - staged_tree (REQUIRED)
//   - signature.public_key_id (Ed25519) OR signature.secret_tier (HMAC)
// ---------------------------------------------------------------------------
function buildVerdict(input) {
  if (!input || typeof input !== 'object') {
    throw new Error('CORTEX_R2_VERDICT_INPUT_REQUIRED');
  }
  const {
    findings,
    applied,
    deferred,
    refuted,
    sprintId,
    workflowRunId,
    agentRoster,
    timestamp,
    decision,
    secret: explicitSecret,
    // v2 inputs
    commitSha,
    stagedTree,
    schemaVersion,
    signatureAlgorithm,
    signingKey, // { privateKeyPem, publicKeyId }
  } = input;

  if (!sprintId) throw new Error('CORTEX_R2_VERDICT_MISSING_SPRINT_ID');
  if (!workflowRunId)
    throw new Error('CORTEX_R2_VERDICT_MISSING_WORKFLOW_RUN_ID');
  if (!timestamp) throw new Error('CORTEX_R2_VERDICT_MISSING_TIMESTAMP');
  if (!Array.isArray(agentRoster))
    throw new Error('CORTEX_R2_VERDICT_MISSING_AGENT_ROSTER');
  if (!findings || typeof findings !== 'object')
    throw new Error('CORTEX_R2_VERDICT_MISSING_FINDINGS');

  const ALLOWED_DECISIONS = ['PASS', 'FAIL'];
  if (!decision || !ALLOWED_DECISIONS.includes(String(decision))) {
    throw new Error('CORTEX_R2_VERDICT_MISSING_DECISION');
  }

  // Decide schema version.
  const useV2 =
    schemaVersion === SCHEMA_VERSION_V2 ||
    (commitSha !== undefined && commitSha !== null) ||
    signatureAlgorithm === SIG_ALG_ED25519;
  const effectiveSchema = useV2 ? SCHEMA_VERSION_V2 : SCHEMA_VERSION;

  const alg = signatureAlgorithm || SIG_ALG;
  if (!SUPPORTED_SIG_ALGS.includes(alg)) {
    throw new Error('CORTEX_R2_VERDICT_UNSUPPORTED_ALG_INPUT');
  }

  if (effectiveSchema === SCHEMA_VERSION_V2) {
    if (!commitSha || typeof commitSha !== 'string') {
      throw new Error('CORTEX_R2_VERDICT_MISSING_COMMIT_SHA');
    }
    if (!stagedTree || typeof stagedTree !== 'string') {
      throw new Error('CORTEX_R2_VERDICT_MISSING_STAGED_TREE');
    }
  }

  const payload = {
    schema_version: effectiveSchema,
    sprint_id: String(sprintId),
    workflow_run_id: String(workflowRunId),
    timestamp: String(timestamp),
    agent_roster: agentRoster.map(String),
    findings: findings,
    applied: Array.isArray(applied) ? applied : [],
    deferred: Array.isArray(deferred) ? deferred : [],
    refuted: Array.isArray(refuted) ? refuted : [],
    decision: String(decision),
  };
  if (effectiveSchema === SCHEMA_VERSION_V2) {
    payload.commit_sha = String(commitSha);
    payload.staged_tree = String(stagedTree);
  }

  let signatureBlock;
  if (alg === SIG_ALG_ED25519) {
    // Resolve signing key — explicit input wins, else load via keys module.
    let priv = signingKey && signingKey.privateKeyPem;
    let kid = signingKey && signingKey.publicKeyId;
    if (!priv || !kid) {
      const keysMod = _lazyKeysModule();
      if (!keysMod || typeof keysMod.loadOrCreateSigningKey !== 'function') {
        throw new Error('CORTEX_R2_VERDICT_ED25519_KEY_MISSING');
      }
      const loaded = keysMod.loadOrCreateSigningKey({
        generateIfMissing: false,
      });
      priv = loaded.privateKeyPem;
      kid = loaded.publicKeyId;
    }
    if (!priv || !kid) {
      throw new Error('CORTEX_R2_VERDICT_ED25519_KEY_MISSING');
    }
    const value = signPayloadEd25519(payload, priv);
    signatureBlock = {
      alg: SIG_ALG_ED25519,
      value,
      public_key_id: String(kid),
    };
  } else {
    // HMAC path — resolve secret + emit tier on v2.
    let secret;
    let secretTier;
    if (typeof explicitSecret === 'string' && explicitSecret.length > 0) {
      secret = explicitSecret;
      secretTier = 'env'; // explicit caller-provided secret == env-tier trust
    } else {
      const resolved = resolveSecret();
      secret = resolved.secret;
      // Map source → tier: 'env'|'file'(persisted)|'host-derived'(host)
      if (resolved.source === 'env') secretTier = 'env';
      else if (resolved.source === 'file') secretTier = 'persisted';
      else secretTier = 'host';
    }
    const value = signPayload(payload, secret);
    signatureBlock = { alg: SIG_ALG, value };
    if (effectiveSchema === SCHEMA_VERSION_V2) {
      signatureBlock.secret_tier = secretTier;
    }
  }

  return Object.assign({}, payload, { signature: signatureBlock });
}

// ---------------------------------------------------------------------------
// verifyVerdict(json, secret, options = {}) → { ok, reason, parsed? }
//
// Options (all OPTIONAL):
//   headSha           string — when set, parsed.commit_sha MUST equal
//   stagedTree        string — when set, parsed.staged_tree MUST equal
//   journalLookup     fn(workflow_run_id) → bool; true = replay, deny
//   strictSecret      bool — when true, host-derived HMAC tier is rejected
//                            AND missing-secret throws (no fail-open)
//   publicKeyRegistry Map<kid, publicKeyPem> for Ed25519 verification
//
// Backward-compat: v1 verdicts skip commit_sha / staged_tree checks (the
// payload simply doesn't carry those fields). journalLookup is enforced on
// BOTH v1 and v2 because the journal is a defense layer independent of
// schema.
// ---------------------------------------------------------------------------
function verifyVerdict(json, secret, options) {
  const opts = options || {};

  let parsed;
  if (typeof json === 'string') {
    try {
      parsed = JSON.parse(json);
    } catch (_) {
      return { ok: false, reason: 'CORTEX_R2_VERDICT_MALFORMED_JSON' };
    }
  } else if (json && typeof json === 'object') {
    parsed = json;
  } else {
    return { ok: false, reason: 'CORTEX_R2_VERDICT_MALFORMED_JSON' };
  }

  if (!parsed || typeof parsed !== 'object') {
    return { ok: false, reason: 'CORTEX_R2_VERDICT_MALFORMED_JSON' };
  }

  if (!SUPPORTED_SCHEMA_VERSIONS.includes(parsed.schema_version)) {
    return {
      ok: false,
      reason: 'CORTEX_R2_VERDICT_SCHEMA_VERSION_MISMATCH',
      parsed,
    };
  }

  const sig = parsed.signature;
  if (!sig || typeof sig !== 'object') {
    return { ok: false, reason: 'CORTEX_R2_VERDICT_MISSING_SIGNATURE', parsed };
  }
  if (!SUPPORTED_SIG_ALGS.includes(sig.alg)) {
    return { ok: false, reason: 'CORTEX_R2_VERDICT_UNSUPPORTED_ALG', parsed };
  }
  if (typeof sig.value !== 'string' || sig.value.length === 0) {
    return { ok: false, reason: 'CORTEX_R2_VERDICT_MISSING_SIGNATURE', parsed };
  }

  // STRICT_SECRET: gate HMAC host-tier and missing secrets.
  if (opts.strictSecret === true && sig.alg === SIG_ALG) {
    // v2 payload carries secret_tier; v1 doesn't.
    if (parsed.schema_version === SCHEMA_VERSION_V2 && sig.secret_tier === 'host') {
      throw new Error('CORTEX_R2_VERDICT_STRICT_SECRET_MISSING');
    }
    if (typeof secret !== 'string' || secret.length === 0) {
      // No secret resolved AND strict → caller must surface, can't fail-open.
      // Try local resolution without auto-generate to give clearer signal.
      const resolved = _resolveSecretNoGenerate();
      if (resolved.source === 'host-derived') {
        throw new Error('CORTEX_R2_VERDICT_STRICT_SECRET_MISSING');
      }
    }
  }

  // v2 commit_sha binding (skipped on v1 — backward-compat).
  if (parsed.schema_version === SCHEMA_VERSION_V2) {
    if (typeof opts.headSha === 'string' && opts.headSha.length > 0) {
      if (parsed.commit_sha !== opts.headSha) {
        return {
          ok: false,
          reason: 'CORTEX_R2_VERDICT_HEAD_MISMATCH',
          parsed,
        };
      }
    }
    if (typeof opts.stagedTree === 'string' && opts.stagedTree.length > 0) {
      if (parsed.staged_tree !== opts.stagedTree) {
        return {
          ok: false,
          reason: 'CORTEX_R2_VERDICT_TREE_MISMATCH',
          parsed,
        };
      }
    }
  }

  // Replay journal — applied to v1 and v2 alike.
  if (typeof opts.journalLookup === 'function') {
    let burned = false;
    try {
      burned = opts.journalLookup(parsed.workflow_run_id) === true;
    } catch (_) {
      burned = false;
    }
    if (burned) {
      return { ok: false, reason: 'CORTEX_R2_VERDICT_RUN_ID_BURNED', parsed };
    }
  }

  // Strip signature before re-canonicalizing.
  const payloadOnly = {};
  for (const k of Object.keys(parsed)) {
    if (k === 'signature') continue;
    payloadOnly[k] = parsed[k];
  }

  if (sig.alg === SIG_ALG_ED25519) {
    const registry = opts.publicKeyRegistry;
    if (!registry || typeof registry.get !== 'function') {
      return { ok: false, reason: 'CORTEX_R2_VERDICT_UNKNOWN_SIGNER', parsed };
    }
    const kid = sig.public_key_id;
    if (typeof kid !== 'string' || kid.length === 0) {
      return { ok: false, reason: 'CORTEX_R2_VERDICT_UNKNOWN_SIGNER', parsed };
    }
    const pub = registry.get(kid);
    if (!pub) {
      return { ok: false, reason: 'CORTEX_R2_VERDICT_UNKNOWN_SIGNER', parsed };
    }
    const ok = verifyPayloadEd25519(payloadOnly, sig.value, pub);
    if (!ok) {
      return {
        ok: false,
        reason: 'CORTEX_R2_VERDICT_SIGNATURE_MISMATCH',
        parsed,
      };
    }
    return { ok: true, reason: 'CORTEX_R2_VERDICT_OK', parsed };
  }

  // HMAC path.
  if (typeof secret !== 'string' || secret.length === 0) {
    // Fail-OPEN: signature present but no secret to verify against (CI gap).
    // (STRICT path above would have already thrown.)
    return {
      ok: true,
      reason: 'CORTEX_R2_VERDICT_NO_SECRET_WARNING',
      parsed,
    };
  }

  const expected = signPayload(payloadOnly, secret);

  const expectedBuf = Buffer.from(expected, 'hex');
  let actualBuf;
  try {
    actualBuf = Buffer.from(sig.value, 'hex');
  } catch (_) {
    return {
      ok: false,
      reason: 'CORTEX_R2_VERDICT_SIGNATURE_MISMATCH',
      parsed,
    };
  }
  if (actualBuf.length !== expectedBuf.length) {
    return {
      ok: false,
      reason: 'CORTEX_R2_VERDICT_SIGNATURE_MISMATCH',
      parsed,
    };
  }
  let match;
  try {
    match = crypto.timingSafeEqual(expectedBuf, actualBuf);
  } catch (_) {
    return {
      ok: false,
      reason: 'CORTEX_R2_VERDICT_SIGNATURE_MISMATCH',
      parsed,
    };
  }
  if (!match) {
    return {
      ok: false,
      reason: 'CORTEX_R2_VERDICT_SIGNATURE_MISMATCH',
      parsed,
    };
  }

  return { ok: true, reason: 'CORTEX_R2_VERDICT_OK', parsed };
}

// ---------------------------------------------------------------------------
// loadVerdict(rootDir) → { json, raw } or null when file absent.
// Returns null on ENOENT; throws on other I/O errors (caller decides).
// ---------------------------------------------------------------------------
function loadVerdict(rootDir) {
  if (!rootDir || typeof rootDir !== 'string') {
    throw new Error('CORTEX_R2_VERDICT_LOAD_ROOT_REQUIRED');
  }
  const filePath = path.join(rootDir, 'cortex', 'r2-verdict.json');
  let raw;
  try {
    raw = fs.readFileSync(filePath, 'utf8');
  } catch (err) {
    if (err && err.code === 'ENOENT') return null;
    throw err;
  }
  let json;
  try {
    json = JSON.parse(raw);
  } catch (_) {
    return { json: null, raw };
  }
  return { json, raw };
}

