// r2-verdict.cjs — signed verdict for R2 review pipeline.
//
// Sprint 2.46: HMAC-SHA256 v1 baseline (schema_version=1).
// Sprint 2.46.1: v2 schema + Ed25519 + STRICT_SECRET — closes 3 R2-HIGH residuals:
//   1. commit_sha + staged_tree bind verdict to the exact tree it unblocks
//   2. journalLookup hook enables single-use replay defense (impl in journal module)
//   3. resolveSecret() returns { secret, source } so STRICT_SECRET=1 can reject
//      host-derived fallback; signature block carries secret_tier or public_key_id.
//
// Zero npm deps — node:crypto + node:fs + node:path + node:os only.
//
// Deterministic guarantee: timestamp, workflowRunId, commitSha, stagedTree are
// INPUTS to buildVerdict(). The module body must NEVER call Date.now(),
// new Date() with no args, crypto.randomUUID(), or crypto.randomBytes().
// (resolveSecret() may auto-generate a persisted file once; that path is gated
// behind a missing-file check and the generated bytes are written, not returned
// to the deterministic signing path.)
//
// Error codes:
//   CORTEX_R2_VERDICT_INPUT_REQUIRED
//   CORTEX_R2_VERDICT_MISSING_SPRINT_ID
//   CORTEX_R2_VERDICT_MISSING_WORKFLOW_RUN_ID
